function createProjects(projectsInfoArray, containerToAppend) {
  for (project of projectsInfoArray) {
    // --------- HTML Elements ---------
    // Project Container
    const projectContainer = document.createElement('div');
    projectContainer.classList.add('project-card');
    // Project Link
    const projectLink = document.createElement('a');
    projectLink.setAttribute('href', project.link);
    projectLink.classList.add('absolute', 'top-0', 'right-0', 'bottom-0', 'left-0');
    // Project Date
    const projectDate = document.createElement('p');
    projectDate.classList.add('text-sm', 'w-full', 'md:text-base', 'xl:text-lg', '2xl:text-xl', '3xl:text-2xl');
    projectDate.innerText = project.date;
    // Project Logo Container
    const projectLogoContainer = document.createElement('figure');
    projectLogoContainer.classList.add('flex-center', 'w-full');
    // Project Logo
    const projectLogo = document.createElement('img');
    projectLogo.setAttribute('src', project.logo);
    projectLogo.setAttribute('alt', project.logoDescription);
    projectLogo.classList.add('w-[70%]', 'object-cover');
    // Project Name Container
    const projectNameContainer = document.createElement('div');
    projectNameContainer.classList.add('flex-center', 'flex-col', 'gap-1', 'uppercase', 'text-center');
    // Project Title
    const projectTitle = document.createElement('p');
    projectTitle.classList.add('md:text-lg', 'xl:text-xl', '2xl:text-2xl', '3xl:text-[1.6rem]');
    projectTitle.innerText = project.title;
    // Project Description
    const projectDescription = document.createElement('p');
    projectDescription.classList.add('font-Roboto', 'text-[0.68rem]', 'leading-3', 'text-gray-400', 'md:text-sm', 'xl:text-base', '2xl:text-[1.125rem]', '3xl:text-[1.2rem]');
    projectDescription.innerText = project.description;
    // Project Technologies Container
    const projectTechnologiesContainer = document.createElement('div');
    projectTechnologiesContainer.classList.add('flex-center', 'flex-wrap', 'gap-x-3', 'w-full', 'pt-2');

    // --------- HTML Layout ---------
    containerToAppend.appendChild(projectContainer);
      
    projectContainer.append(projectLink, projectDate, projectLogoContainer, projectNameContainer, projectTechnologiesContainer);

    projectLogoContainer.appendChild(projectLogo);

    projectNameContainer.append(projectTitle, projectDescription);
    
    // --------- Adding Technologies Icons ---------
    // Only when adding HTML Icon
    if (project.html.display === true) {
      createTechnologyIcon(project.html, projectTechnologiesContainer);
    }
    // Only when adding CSS Icon
    if (project.css.display === true) {
      createTechnologyIcon(project.css, projectTechnologiesContainer);
    }
    // Only when adding SASS Icon
    if (project.sass.display === true) {
      createTechnologyIcon(project.sass, projectTechnologiesContainer);
    }
    // Only when adding Tailwind Icon
    if (project.tailwind.display === true) {
      createTechnologyIcon(project.tailwind, projectTechnologiesContainer);
    }
    // Only when adding JavaScript Icon
    if (project.javascript.display === true) {
      createTechnologyIcon(project.javascript, projectTechnologiesContainer);
    }
  }
}

function createTechnologyIcon(technology, containerToAppend) {
  // --------- HTML Elements ---------
  const projectTechnologyContainer = document.createElement('figure');
  projectTechnologyContainer.classList.add('md:w-11', 'xl:w-12', '2xl:w-14', '3xl:w-16');
  const projectTechnologyIcon = document.createElement('img');
  projectTechnologyIcon.setAttribute('src', technology.icon);
  projectTechnologyIcon.setAttribute('alt', technology.description);
  projectTechnologyIcon.classList.add('md:w-full');

  // --------- HTML Layout ---------
  containerToAppend.appendChild(projectTechnologyContainer);
  projectTechnologyContainer.appendChild(projectTechnologyIcon);
}

